#include <iostream>

using namespace std;

int main()
{
    int a=2;
    int b=4;

    cout <<"the number before increment:"<<a<<  endl;
    a++;
    cout <<"the number after increment:"<<a<<  endl;

    cout <<"the number before decrement:"<<b<<  endl;
    b--;

    cout <<"the number after decrement:"<<b<<  endl;
    return 0;
}
